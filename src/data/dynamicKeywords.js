export const getKeywordBySlug = (slug) => {
  return dynamicKeywords.find((kw) => kw.slug === slug)
}

export const getAllSlugs = () => {
  return dynamicKeywords.map((kw) => ({ params: { slug: kw.slug } }))
}

export const getKeywordsByLocation = (location) => {
  return dynamicKeywords.filter((kw) => kw.location === location)
}

export const getKeywordsByService = (service) => {
  return dynamicKeywords.filter((kw) => kw.service === service)
}

export const getAllUniqueServices = () => {
  const services = new Set(dynamicKeywords.map((kw) => kw.service))
  return Array.from(services).sort()
}

export const getAllUniqueLocations = () => {
  const locations = new Set(dynamicKeywords.map((kw) => kw.location))
  return Array.from(locations).sort()
}

export const getLocationServiceCombinations = () => {
  const combinations = dynamicKeywords.map((kw) => ({
    location: kw.location,
    service: kw.service,
    slug: kw.slug,
  }))
  return combinations
}
