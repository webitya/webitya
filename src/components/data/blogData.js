// Combine all articles
export const allArticles = [
  ...aiArticles,
  ...pythonArticles,
  ...javascriptArticles,
  ...nextjsArticles,
  ...digitalMarketingArticles,
]

// Get recent articles (published in the last 30 days)
export const getRecentArticles = () => {
  return allArticles.filter((article) => article.isRecent).slice(0, 6)
}

// Get popular articles
export const getPopularArticles = () => {
  return allArticles.filter((article) => article.isPopular).slice(0, 6)
}

// Get articles by category
export const getArticlesByCategory = (category) => {
  if (category === "All") return allArticles
  return allArticles.filter((article) => article.category === category)
}

// Search articles
export const searchArticles = (query) => {
  const lowercaseQuery = query.toLowerCase()
  return allArticles.filter(
    (article) =>
      article.title.toLowerCase().includes(lowercaseQuery) ||
      article.excerpt.toLowerCase().includes(lowercaseQuery) ||
      article.tags.some((tag) => tag.toLowerCase().includes(lowercaseQuery)),
  )
}

// Get article by slug
export const getArticleBySlug = (slug) => {
  return allArticles.find((article) => article.slug === slug)
}

// Get related articles
export const getRelatedArticles = (currentArticle, limit = 3) => {
  return allArticles
    .filter(
      (article) =>
        article.id !== currentArticle.id &&
        (article.category === currentArticle.category || article.tags.some((tag) => currentArticle.tags.includes(tag))),
    )
    .slice(0, limit)
}
